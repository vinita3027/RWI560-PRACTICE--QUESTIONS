package com.String;

public class StringConcatMethod {
	 public static void main(String[]args) {
		 System.out.println(10+"");
		 System.out.println(10+""+20);
		 System.out.println(10+""+20+30);
		 System.out.println(10+20+""+30+40);
		 System.out.println("ABC"+2.5);
		 System.out.println("ABC"+'D');
		 System.out.println("ABC"+true);
		 System.out.println("ABC"+null);
		 System.out.println("ABC"+25L);
		 System.out.println("ABC"+2.50f);
		 System.out.println("ABC"+25==null);
	 }
}
